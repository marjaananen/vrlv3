        <p><b>Huom!</b> Tämä osuus koskee vain perinteisiä kilpailuja. Porrastettujen kilpailujen tuloksia ei erikseen tuomaroida, vaan VRL arpoo ne kisapäivänä automaattisesti.</p>
        <b>Arvontatavat</b>
        <ul>
            <li><b>Lyhyt arvonta</b>: Osallistujalista arvotaan kertaalleen jollain vapaavalintaisella arvontakoneella</li>
            <li><b>Pitkä arvonta</b>: Osallistujille arvotaan useita tuloksia (esim. osakokeet erikseen), joista yhteissumma ratkaisee. </li>
            <li><b>Suhteutettu arvonta</b>: Suhteutetussa arvonnassa arvontatulokseen vaikuttaa jokin hevosen keräämä ominaisuus, esim. aiempi sijoitusprosentti tms. Porrastetutkin ovat eräänlainen suhteutettu arvonta.</li>
            <li><b>Tuotosluokat</b>: Osallistujille jaetaan pisteitä jonkin tuotoksen, esim. tarinan, runon, piirroksen yms. tuotoksen mukaan.</li>
            <li><b>Kysymykset</b>: Osallistujat vastaavat kysymyksiin, ja tulos määräytyy niiden mukaan. Tasatuloksessa lopputulos usein arvotaan.</li>

        </ul>
