<h2>VRL:n kilpailutoiminta</h2>

<p>VRL:n alaisesta kilpailutoiminnasta vastaavat kilpailujaokset.
Osallistuaksesi VRL:n alaisiin kilpailuihin sinulla tulee olla VRL-tunnus.
Joissakin kilpailuissa edellytetään myös hevosen rekisteröintiä.</p>

<p>Virtuaalihevosmaailmassa kilpailuja on kahdentyyppisiä. Porrastetuissa kilpailuissa VRL laskee tulokset hevosen ominaisuuksien perusteella,
perinteisissä kilpailuissa kilpailun järjestäjä arpoo tai muulla tavoin tuottaa tulokset esim. osallistujien tarinoita arvostellen, ja ilmoittaa ne VRL:lle.
Kilpailuiden järjestämisestä ja niihin osallistumisesta lisää kilpailusääntösivulla.</p>

<p>Kilpailuita, jotka eivät ole VRL:n jaosten alaisia kutsutaan "villeiksi kilpailuiksi". Näissä kilpailuissa VRL ei vastaa säännöistä eikä tuloksista.</p>

<p>Uusia kilpailujaoksia voidaan hyväksyä VRL:n alaisuuteen, mikäli ylläpito ja jaosvastaava näin päättävät.</p>
